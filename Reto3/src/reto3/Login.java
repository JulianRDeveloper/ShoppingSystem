    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        ///////////////////////////////////////
            String user= txtUser.getText();
            String password= txtPassword.getText();
        if(!user.isEmpty() && !password.isEmpty()){
            LinkedList list= ctlClient.verifyLogin(user, password);
            System.out.println(list);
            try{
                if(!list.isEmpty()){
                    int clientID= (Integer)list.get(0);
                    int clientType= (Integer)list.get(1);
                    if(clientType==1){
                        natClient = ctlClient.loadNaturalClient(clientID);
                        this.dispose();
                        app= new reto3JFrame(natClient);
                        app.setVisible(true);                        
                    }else if(clientType==2){
                        legClient = ctlClient.loadLegalClient(clientID);
                        this.dispose();
                        app= new reto3JFrame(legClient);
                        app.setVisible(true);
                    }
                    }     
            }catch(Exception e){
                JOptionPane.showMessageDialog(this, "Wrong introduced username/password ");
                txtUser.setText("");
                txtPassword.setText("");
            }
        }else{
            JOptionPane.showMessageDialog(this, "Has left empty fields");
        }  
    }//GEN-LAST:event_btnLoginActionPerformed

