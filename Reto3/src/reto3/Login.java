    private boolean validateFormulary(){
        if(!rb_person.isSelected() && !rb_company.isSelected()){
                JOptionPane.showMessageDialog(FFormulary, "Select a Person or Company");
            }
        else if(rb_company.isSelected()){
            //System.out.println("company is selected");
            String company = txt_company.getText();
            String NIT = txt_NIT.getText();
            String address = txt_address.getText();
            String phone = txt_phone.getText();
            String mail = txt_mail.getText();
            String user = txtFrmUser.getText();
            String password= txtFrmPassword.getText();
            String pass= txtFrmPass.getText();           
            //int id= 0;            
            objFormulary = new Formulary(address, phone, mail, company, NIT, user, password, pass, 2);
            if(!objFormulary.companyValidation()){
                JOptionPane.showMessageDialog(FFormulary, "You must not leave empty company fields!");
                objFormulary = null;
                return false;
            }else{
                if(!objFormulary.userKeyValidation()){
                     JOptionPane.showMessageDialog(FFormulary, "Passwords don't match");
                     objFormulary = null;
                     return false;
                }else{
                    return true;
                }
            }            
        }        
        else if (rb_person.isSelected()) {
            //System.out.println("persona is selected");
            String identify = txt_identify.getText();
            String names = txt_names.getText();
            String lastnames = txt_lastnames.getText();
            String address = txt_address.getText();
            String phone = txt_phone.getText();
            String mail = txt_mail.getText();
            String user = txtFrmUser.getText();
            String password= txtFrmPassword.getText();
            String pass= txtFrmPass.getText();
            //int id= 0;
            objFormulary = new Formulary(address, phone, mail, identify, names, lastnames, user, password, pass, 1);
            if(!objFormulary.personaSelfValidation()){
                JOptionPane.showMessageDialog(FFormulary, "You must not leave empty persona fields!");
                objFormulary = null;
                return false;
            }else{
                if(!objFormulary.userKeyValidation()){
                     JOptionPane.showMessageDialog(FFormulary, "Passwords don't match");
                     objFormulary = null;
                     return false;
                }else{
                    return true;
                }
            }
        }return false;
    }
    
