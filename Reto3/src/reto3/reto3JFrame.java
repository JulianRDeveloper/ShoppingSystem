    private ShopCart refreshShopCart(JTable model){
        try{
            shopCart = ctlCart.loadShopCart(clientID);
                
            txt_cartTimeLess.setText(shopCart.timeLess());                                        
            txt_cartID.setText(Integer.toString(shopCart.getId()));
            txt_cartTimeLess.setText(shopCart.timeLess());
            txt_itemsCart.setText(shopCart.getAmounts());
            txt_cartCost.setText(shopCart.getTotalPrice());
                
            cleanCartTable();
            cartStock = ctlCart.updateShopCart(model, shopCart.getId());        
            cartTable.setModel(cartStock);
                
            if(shopCart.checkTimeLimi()){
                delShopCart();
            }
            return shopCart;
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "You must have an active shopping cart to select your products, this cart will expire in 24 hours ");
        } 
        return null;
    }
       
