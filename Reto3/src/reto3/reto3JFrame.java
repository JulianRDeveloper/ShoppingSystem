    private boolean recHistorical(String orderStatus){
        switch(orderStatus){
            case "FINISHED" -> {       
                int items = Integer.parseInt(shopCart.getAmounts());
                double cost = Double.parseDouble(shopCart.getTotalPrice());
                Historical his = new Historical(order.getId(), order.getOrderDate(), "FINISHED", cost, items, pay.getPayDate(), send.getSendDate(), send.getSendCompany(), clientID);
                if(ctlHistory.addHistory(his)){
                    return true;
                }else{
                    return false;
                }   }
            case "CANCELED" -> {
                int items = Integer.parseInt(shopCart.getAmounts());
                double cost = Double.parseDouble(shopCart.getTotalPrice());
                Historical his = new Historical(order.getId(), order.getOrderDate(), "CANCELED", cost, items, null, null, "NOT COMPANY", clientID);
                if(ctlHistory.addHistory(his)){
                    return true;
                }else{
                    return false;
                }       }
        }
        return false;
    }
    
